package main

import "fmt"

func main() {
	var age int = 29 // variable declaration + initialization in a single line example
	fmt.Println("my age is ", age)
	//age = 29 // assigning variable value 1
	fmt.Println("my age is still", age)
	age = 54 // assigning variable value 2
	fmt.Println("my updated age is ", age)

	fmt.Println("") // free line

	
}