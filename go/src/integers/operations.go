package integers

/*Add ... Function Specification:
INPUTS:
	number1 = first number to add
	number2 = second number to add
OUTPUTS:
	Returns the sum of the two inputted numbers
*/
func Add(number1, number2 int) int {
	return number1 + number2
}

/*Subtract ... Function Specification:
INPUTS:
	number1 = first number to find difference of
	number2 = second number to find difference of
OUTPUTS:
	Returns the difference between the first and second inputted numbers
*/
func Subtract(number1, number2 int) int {
	return number1 - number2
}

/*Multiply ... Function Specification:
INPUTS:
	number1 = first number to multiply
	number2 = second number to multiply
OUTPUTS:
	Returns the product of the first and second inputted numbers
*/
func Multiply(number1, number2 int) int {
	return number1 * number2
}

/*Divide ... Function Specification:
INPUTS:
	number1 = first number to multiply
	number2 = second number to multiply
OUTPUTS:
	Returns the result of the first input divided by the second input
*/
func Divide(number1, number2 int) int {
	return number1 / number2
}
